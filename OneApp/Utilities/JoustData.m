#import "JoustData.h"

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
@implementation JoustData

@synthesize language = _language;
@synthesize locationCount = _locationCount;
@synthesize puzzleImageURL = _puzzleImageURL;
@synthesize puzzleImageID = _puzzleImageID;

////////////////////////////////////////////////////////////////////////////////
- (id) init
{
	self = [super init];
	if (self != nil) 
	{
		[JoustDebug log:@"INIT JOUST DATA SINGLETON"];
		_language = @"en";
	}
	
	return self;
}

////////////////////////////////////////////////////////////////////////////
+ (id)sharedInstance
{
	static dispatch_once_t pred = 0;
	__strong static id _sharedObject = nil;
	dispatch_once(&pred, ^{
		_sharedObject = [[self alloc] init]; // or some other init method
	});
	return _sharedObject;
}

@end
